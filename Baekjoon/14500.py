import sys
N, M = map(int, sys.stdin.readline().split())
s = [list(map(int, sys.stdin.readline().split())) for _ in range(N)]

t = [
    [[0, 1], [0, 2], [0, 3]], #type1
    [[1, 0], [2, 0], [3, 0]], 
    [[0, 1], [1, 0], [1, 1]], #type2
    [[0, 1], [0, 2], [-1, 2]], #type3
    [[1, 0], [2, 0], [2, 1]],
    [[0, 1], [0, 2], [1, 0]], 
    [[0, 1], [1, 1], [2, 1]], 
    [[1, 0], [1, 1], [1, 2]], 
    [[1, 0], [2, 0], [2, -1]],
    [[0, 1], [0, 2], [1, 2]],
    [[0, 1], [1, 0], [2, 0]],
    [[1, 0], [1, 1], [2, 1]], #type4
    [[0, 1], [1, 0], [1, -1]],
    [[0, 1], [-1, 1], [1, 0]],
    [[0, 1], [1, 1], [1, 2]], 
    [[0, 1], [0, 2], [1, 1]], #type5
    [[1, 0], [1, 1], [1, -1]], 
    [[1, 0], [2, 0], [1, 1]],
    [[1, 0], [2, 0], [1, -1]]
]
res = 0
for i in range(N):
    for j in range(M):
        for k in range(19):
            tmp = s[i][j]
            for l in range(3):
                if (i + t[k][l][0]) <= (N-1) and (j + t[k][l][1]) <= (M-1):
                    tmp += s[i+ t[k][l][0]][j + t[k][l][1]]
                else:
                    tmp = 0
                    break
            res = max(res, tmp)
            
print(res)
